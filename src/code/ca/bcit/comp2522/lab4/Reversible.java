package ca.bcit.comp2522.lab4;

/**
 * Represents an object whose data can be reversed.
 * -
 * Classes that implement this interface must provide a
 * reversed version of their content.
 *
 * @author Jemsel Jumapit
 * @author Faida Espiritu
 *
 * @author 2026
 */
public interface Reversible
{
    /**
     * Returns a reversed representation of the object.
     *
     * @return a String containing the reversed data
     */
    String backward();
}